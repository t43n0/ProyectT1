    private void agregarNota(String nombreAlumno, String asignatura, double notaExamen, double notaActividades, double notaFinal) {
        NotasAlumnAsig nota = new NotasAlumnAsig(nombreAlumno, asignatura, notaExamen, notaActividades, notaFinal);
        listaNotas.add(nota);
    }

    public NotasAlumnAsig buscarNota(String nombre, String asignatura) {
        for (NotasAlumnAsig nota : listaNotas) {
            if (nota.esIgual(nombre, asignatura)) {
                return nota;
            }
        }
        return null;
    }

    public ArrayList<NotasAlumnAsig> obtenerNotasPorAlumno(String nombreAlumno) {
        ArrayList<NotasAlumnAsig> notasAlumno = new ArrayList<NotasAlumnAsig>();
        for (NotasAlumnAsig nota : listaNotas) {
            if (nota.getNombreAlumno().equals(nombreAlumno) && nota.getNotaFinal() != 0) {
                notasAlumno.add(nota);
            }
        }
        return notasAlumno;
    }
